
public class Inputs {
	
	private double landTime;
	private double takeoffTime;
	private double AvgTimeLand;
	private double AvgTimeTakeOff;
	private double totalTime;
	private double maxQueueTime;


	public double getLandTime() {
		return landTime;
	}
	public void setLandTime(double landTime) {
		this.landTime = landTime;
	}
	public double getTakeoffTime() {
		return takeoffTime;
	}
	public void setTakeoffTime(double takeoffTime) {
		this.takeoffTime = takeoffTime;
	}
	public double getAvgTimeLand() {
		return AvgTimeLand;
	}
	public void setAvgTimeLand(double avgTimeLand) {
		AvgTimeLand = avgTimeLand;
	}
	public double getAvgTimeTakeOff() {
		return AvgTimeTakeOff;
	}
	public void setAvgTimeTakeOff(double avgTimeTakeOff) {
		AvgTimeTakeOff = avgTimeTakeOff;
	}
	public double getTotalTime() {
		return totalTime;
	}
	public void setTotalTime(double totalTime) {
		this.totalTime = totalTime;
	}
	public double getMaxQueueTime() {
		return maxQueueTime;
	}
	public void setMaxQueueTime(double maxQueueTime) {
		this.maxQueueTime = maxQueueTime;
	}


}
